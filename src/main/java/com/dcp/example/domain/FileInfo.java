package com.dcp.example.domain;

public class FileInfo {
	
	private int fId;
	private int fMaster;
	private String fName;
	
	public int getfId() {
		return fId;
	}
	public void setfId(int fId) {
		this.fId = fId;
	}
	public int getfMaster() {
		return fMaster;
	}
	public void setfMaster(int fMaster) {
		this.fMaster = fMaster;
	}
	public String getfName() {
		return fName;
	}
	public void setfName(String fName) {
		this.fName = fName;
	}
	

}
